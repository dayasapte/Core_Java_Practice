package july_3;

public class WhileLoopExample2 {

	public static void main(String[] args) {
		
		//print values from 10 to 1--descending order
		
		int i = 10;// Initialization- declaration & assignment
		while(i>=1) { //condition
			System.out.println(i); //statement
			i--; //decrement
		}

	}

}
