package june_28;

public class TernaryOperator {

	public static void main(String[] args) {
		//Ternary Operator ?:
		//variable = expression ? result1 : result2
		
		int x = 200;
		int y = 500;
		
		int result = (x>y) ? x : y ;
		System.out.println(result);   //500
		
		int a=10;
		int b =5;
		int result1 = a>b ? a:b;
		System.out.println(result1);
		

	}

}
