package june_27;

public class DataType2 {

	public static void main(String[] args) {
		
		//Different approaches to create variables
		
		//Approach-1
		int a; //declaration
		a=10;  //assignment
		int b;
		b=20;
		
		//Approach-2
		int a1,b1; //multiple variable declaration
		a1=50;     //Assignment
		b1=60;     //Assignment
		
		//Approach-3
		int a2=30,b2=60; //Multiple variable declaration and assignment
		
		

	}

}
