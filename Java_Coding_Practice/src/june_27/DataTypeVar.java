package june_27;

public class DataTypeVar {

	public static void main(String[] args) {
		
		var variable1 = 10;
		System.out.println(variable1);
		
		var v1 = 23.45;
		System.out.println(v1);
		
		var v2="Daya";
		System.out.println(v2);
		
		var v3 ='R';
		System.out.println(v3);
	}

}
