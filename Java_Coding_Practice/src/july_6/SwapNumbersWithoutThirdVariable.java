package july_6;

public class SwapNumbersWithoutThirdVariable {

	public static void main(String[] args) {
		
		//swapn 2 numbers without 3rd variable
		
		int a=10,b=50;
		
		a=a+b; //60
		
		b=a-b; //10
		
		a=a-b; //50
		
		System.out.println(a);
		System.out.println(b);
		

	}

}
